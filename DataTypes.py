#Datatypes - defines the type of data stored in a variable

#Basic - int,float,string,boolean,complex
num = 10
print(type(num)) #int datatype

complex_num = 3+4j #complex number form - (realNum)+(imaginaryNum)j
print(type(complex_num)) #complex datatype

num1=4.45
print(type(num1)) #float datatype

var = None
print(type(var)) #NoneType datatype

isBool = True
print(type(isBool)) #boolean datatype

name='Archana'
print(type(name)) #string datatype

l1=[1,2,2]
print(type(l1)) #list datatype

tuple1 = (1,2,3,4)
print(type(tuple1)) #tuple datatype

set1={1,2,6}
print(type(set1)) #set datatype

dictionary = {1:'2',3:'4'}
print(type(dictionary)) #dict datatype

